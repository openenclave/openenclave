static void TestCertVerify()
{
    printf("=== begin TestCertVerifyCert()\n");

    {
        OE_Result r;
        OE_VerifyCertError error;
        OE_Cert* cert = NULL;
        OE_CertChain* chain = NULL;
        OE_CertChain* badChain = NULL;
        OE_CRL* crl = NULL;

        r = OE_CertReadPEM(CERT, sizeof(CERT), &cert);
        assert(r == OE_OK);

        r = OE_CertChainReadPEM(CHAIN, sizeof(CHAIN), &chain);
        assert(r == OE_OK);

        /* Chain does not contain a root for this certificate */
        r = OE_CertChainReadPEM(BAD_CHAIN, sizeof(BAD_CHAIN), &badChain);
        assert(r == OE_OK);

        r = OE_CertVerify(cert, chain, crl, &error);
        assert(r == OE_OK);

        r = OE_CertVerify(cert, badChain, crl, &error);
        assert(r == OE_VERIFY_FAILED);

        OE_CertFree(cert);
        OE_CertChainFree(chain);
        OE_CertChainFree(badChain);
    }

    TestCertVerifyGood();
    TestCertVerifyBad();

    printf("=== passed TestCertVerifyCert()\n");
}

static void TestRandom()
{
    printf("=== begin TestRandom()\n");

    static const size_t N = 64;
    static const size_t M = 19;
    uint8_t buf[N][M];

    memset(buf, 0, sizeof(buf));

    for (size_t i = 0; i < N; i++)
    {
        /* Generate a random sequence */
        if (OE_Random(buf[i], M * sizeof(uint8_t)) != OE_OK)
            OE_Assert("OE_Random() failed" == NULL);

#if 0
        OE_HexDump(buf[i], M * sizeof(uint8_t));
#endif

        /* Be sure buffer is not filled with same character */
        {
            size_t m;
            uint8_t c = buf[i][0];

            for (m = 1; m < M && buf[i][m] == c; m++)
                ;

            assert(m != M);
        }

        /* Check whether duplicate of one of the previous calls */
        for (size_t j = 0; j < i; j++)
        {
            assert(memcmp(buf[j], buf[i], M * sizeof(uint8_t)) != 0);
        }
    }

    printf("=== passed TestRandom()\n");
}

